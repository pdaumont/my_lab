#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*s1;

	i = 0;
	s1 = (char *)b;
	while (i < len)
	{
		s1[i] = c;
		i++;
	}
	return (b);
}
