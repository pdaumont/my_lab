#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	len;
	size_t	i;

	if (s1 && s2)
	{
		len = ft_strlen(s1);
		i = 0;
		while (s2[i] != '\0')
		{
			s1[i + len] = s2[i];
			i++;
		}
		s1[i + len] = '\0';
	}
	return (s1);
}
